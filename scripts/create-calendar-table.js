async function run() {
  const client = new Client({ connectionString: process.env.DATABASE_URL, ssl: { rejectUnauthorized: false } });
  try {
    await client.connect();
    console.log('Connected.');
    await client.query(`
      CREATE TABLE IF NOT EXISTS calendar_data (
        id SERIAL PRIMARY KEY,
        month_name VARCHAR(30) NOT NULL,
        month_index INTEGER NOT NULL UNIQUE,
        days JSONB NOT NULL DEFAULT '[]',
        updated_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT NOW()
      );
    `);
    console.log('Table created.');
    // Seed data
    for (let i = 0; i < MONTHS.length; i++) {
      const m = MONTHS[i];
      await client.query(
        `INSERT INTO calendar_data (month_name, month_index, days) VALUES ($1, $2, $3)
         ON CONFLICT (month_index) DO UPDATE SET month_name=$1, days=$3, updated_at=NOW()`,
        [m.name, i + 1, JSON.stringify(m.days)]
      );
    }
    console.log('Seeded all 12 months!');
  } catch (e) { console.error(e); } finally { await client.end(); }
}
run();
